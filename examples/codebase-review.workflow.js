// ──────────────────── PATHS ────────────────────
const PROJECT_ROOT = "E:\\GodotProjects\\advanced-fps-controller-template";
const fs = await import("node:fs/promises");
const path = await import("node:path");

// ──────────────────── SHARED STATE ────────────────────
// Single source of truth. status transitions:
//   pending  (just reported by a reviewer)
//      ↓ claim_finding
//   in-review (held by a specific triager)
//      ↓ keep_finding / drop_finding
//   kept | dropped (terminal)
//
// Single-threaded JS → no locks needed.
let counter = 0;
const findings = [];
const pending  = () => findings.filter(f => f.status === "pending");
const inReview = () => findings.filter(f => f.status === "in-review");
const byStatus = (s) => findings.filter(f => f.status === s).length;

// ──────────────────── TOOLS ────────────────────
// report_finding — reviewer pushes one issue onto the queue. No batch
// cap; reviewers are unbounded by design.
defineTool(
  "report_finding",
  {
    description: [
      "Report ONE code-review finding. Call this once per distinct issue you spot.",
      "There is NO cap on how many findings you may report — call as often as the codebase warrants.",
      "Returns the assigned id and the current queue size.",
    ].join(" "),
    inputSchema: {
      type: "object",
      required: ["file", "severity", "rule", "detail"],
      properties: {
        file: {
          type: "string",
          description: "Path to the file containing the issue (relative to project root preferred; absolute also accepted).",
        },
        line: {
          type: "integer",
          description: "1-based line number where the issue is.",
        },
        severity: {
          enum: ["must-fix", "should-fix", "nit"],
          description: "must-fix = correctness/safety bug; should-fix = best-practice violation per CLAUDE.md; nit = style.",
        },
        rule: {
          type: "string",
          description: "Short kebab-case tag identifying the rule violated (e.g. 'var-godot-type', 'bypass-damage-events').",
        },
        detail: {
          type: "string",
          description: "≤500 chars describing what the code does wrong.",
        },
        suggestion: {
          type: "string",
          description: "Optional one-line fix hint.",
        },
      },
    },
  },
  ({ file, line, severity, rule, detail, suggestion }) => {
    const id = ++counter;
    findings.push({
      id,
      status: "pending",
      reporter: null, // filled in by the runner if it surfaces calling-agent context; harmless if not
      reviewer: null,
      file,
      line: Number.isInteger(line) ? line : null,
      severity,
      rule,
      detail,
      suggestion: typeof suggestion === "string" && suggestion ? suggestion : null,
      triage_rationale: null,
    });
    return { ok: true, id, total: findings.length };
  },
);

// claim_finding — triager pops the next pending finding (FIFO), flips
// status to in-review, returns the full payload so the triager doesn't
// need a separate read step.
defineTool(
  "claim_finding",
  {
    description: [
      "Claim the next pending finding for review. Atomically flips its status to in-review so other triagers skip it.",
      "Returns the full finding payload, OR { done: true, queue_empty: true } when nothing is pending.",
      "Stop calling immediately when you see done:true and emit your final { processed } JSON.",
      "Pass `triager` set to a stable label for this session (used by stale-claim recovery).",
    ].join(" "),
    inputSchema: {
      type: "object",
      required: ["triager"],
      properties: {
        triager: {
          type: "string",
          description: "Your session label, e.g. 'triage-w1[0]'. Used to attribute the claim.",
        },
      },
    },
  },
  ({ triager }) => {
    const f = findings.find(x => x.status === "pending");
    if (!f) return { done: true, queue_empty: true };
    f.status = "in-review";
    f.reviewer = String(triager || "unknown");
    return {
      done: false,
      finding: {
        id: f.id,
        file: f.file,
        line: f.line,
        severity: f.severity,
        rule: f.rule,
        detail: f.detail,
        suggestion: f.suggestion,
      },
      remaining_pending: pending().length,
    };
  },
);

// keep_finding — triager confirms the issue is real. Flip to kept.
defineTool(
  "keep_finding",
  {
    description: [
      "Confirm a previously-claimed finding is a real issue. Sets its status to 'kept'.",
      "You MUST have claimed it first (via claim_finding) using the same triager label.",
    ].join(" "),
    inputSchema: {
      type: "object",
      required: ["id", "triager", "rationale"],
      properties: {
        id: { type: "integer", description: "Finding id from claim_finding." },
        triager: { type: "string", description: "Same session label you passed to claim_finding." },
        rationale: { type: "string", description: "Why the finding is real. ≤300 chars." },
        confirmed_severity: {
          enum: ["must-fix", "should-fix", "nit"],
          description: "Optional severity correction. Omit to keep reviewer's severity.",
        },
      },
    },
  },
  ({ id, triager, rationale, confirmed_severity }) => {
    const f = findings.find(x => x.id === id);
    if (!f) return { ok: false, error: "not-found" };
    if (f.status !== "in-review") return { ok: false, error: `not-in-review (status=${f.status})` };
    if (f.reviewer !== String(triager)) return { ok: false, error: "not-claimed-by-you", claimed_by: f.reviewer };
    f.status = "kept";
    f.triage_rationale = String(rationale || "").slice(0, 1000);
    if (confirmed_severity) f.severity = confirmed_severity;
    return { ok: true, kept_count: byStatus("kept") };
  },
);

// drop_finding — triager rejects the finding as hallucination / out of
// scope / duplicate / non-issue. Flip to dropped.
defineTool(
  "drop_finding",
  {
    description: [
      "Reject a previously-claimed finding as a hallucination / non-issue / out of scope. Sets its status to 'dropped'.",
      "You MUST have claimed it first (via claim_finding) using the same triager label.",
    ].join(" "),
    inputSchema: {
      type: "object",
      required: ["id", "triager", "rationale"],
      properties: {
        id: { type: "integer" },
        triager: { type: "string" },
        rationale: { type: "string", description: "Why the finding is NOT real. ≤300 chars." },
      },
    },
  },
  ({ id, triager, rationale }) => {
    const f = findings.find(x => x.id === id);
    if (!f) return { ok: false, error: "not-found" };
    if (f.status !== "in-review") return { ok: false, error: `not-in-review (status=${f.status})` };
    if (f.reviewer !== String(triager)) return { ok: false, error: "not-claimed-by-you", claimed_by: f.reviewer };
    f.status = "dropped";
    f.triage_rationale = String(rationale || "").slice(0, 1000);
    return { ok: true, dropped_count: byStatus("dropped") };
  },
);


// ──────────────────── READ-ONLY TOOL SURFACE ────────────────────
const READ_ONLY_TOOLS = {
  bash:       false,
  shell:      false,
  powershell: false,
  cmd:        false,
  exec:       false,
  edit:       false,
  write:      false,
  todowrite:  false,
  todoread:   false,
  // Read, grep, glob, plus our four custom tools remain available.
};

// ──────────────────── PROMPTS ────────────────────
const surveyPrompt = (bucket) => {
  const lines = [];
  lines.push(`# Paths`);
  lines.push(``);
  for (const p of bucket) {
    const win = p.replace(/\//g, "\\");
    lines.push(`  - ${PROJECT_ROOT}\\${win}`);
  }
  lines.push(``);
  lines.push(`# Reviewer focus`);
  lines.push(``);
  lines.push(REVIEW_PROMPT);
  return lines.join("\n");
};

const triagePrompt = (label) => {
  const lines = [];
  lines.push(`# Triager label`);
  lines.push(``);
  lines.push(label);
  lines.push(``);
  lines.push(`Pass exactly this string as the \`triager\` field on every`);
  lines.push(`claim_finding / keep_finding / drop_finding call this session.`);
  lines.push(``);
  lines.push(`Process up to ${TRIAGE_BATCH} findings, then stop.`);
  return lines.join("\n");
};

// ──────────────────── SCHEMAS ────────────────────
const SURVEY_DONE_SCHEMA = {
  type: "object",
  required: ["done"],
  properties: {
    done: { type: "boolean", description: "true iff you've reviewed every assigned path and reported every issue." },
    reported: { type: "integer", description: "Best-effort count of report_finding calls you made." },
  },
};

const TRIAGE_DONE_SCHEMA = {
  type: "object",
  required: ["processed"],
  properties: {
    processed: { type: "integer", description: "Number of findings you claimed and decided this session." },
    note: { type: ["string", "null"], description: "Optional caveat. Omit when nothing to say." },
  },
};

// ──────────────────── PHASE 1: SURVEY ────────────────────
if (PATH_BUCKETS.length === 0) {
  log("FATAL: PATH_BUCKETS is empty — paste the indexer agent's output into the workflow file before running.");
  return { error: "path-buckets-empty" };
}

phase("Survey");
log(`survey: ${PATH_BUCKETS.length} bucket(s), concurrency=${CONCURRENCY}`);

// Bounded in-flight semaphore (matches other workflows' pattern).
let inFlight = 0;
const waiters = [];
async function withSlot(fn) {
  while (inFlight >= CONCURRENCY) {
    await new Promise(resolve => waiters.push(resolve));
  }
  inFlight++;
  try { return await fn(); }
  finally {
    inFlight--;
    const next = waiters.shift();
    if (next) next();
  }
}

const surveyResults = await Promise.all(
  PATH_BUCKETS.map((bucket, i) =>
    withSlot(() =>
      agent(surveyPrompt(bucket), {
        agent: "review-survey",
        label: `survey[${i}]:${bucket[0] || "empty"}`,
        phase: "Survey",
        schema: SURVEY_DONE_SCHEMA,
        tools: READ_ONLY_TOOLS,
      }).then(r => ({
        bucket: i,
        ok: !!r,
        reported: r ? (r.reported || 0) : 0,
      })),
    ),
  ),
);

const surveyOk     = surveyResults.filter(r => r.ok).length;
const surveyFailed = surveyResults.length - surveyOk;
log(`survey done: ${surveyOk}/${surveyResults.length} reviewers completed, ${findings.length} findings queued (${surveyFailed} reviewer agents failed)`);

// ──────────────────── PHASE 2: TRIAGE ────────────────────
phase("Triage");

if (findings.length === 0) {
  log("no findings to triage");
} else {
  let wave = 0;
  while (pending().length > 0 && wave < MAX_TRIAGE_WAVES) {
    wave++;
    const slots = Math.min(
      Math.ceil(pending().length / TRIAGE_BATCH),
      CONCURRENCY,
    );
    log(`triage wave ${wave}: ${pending().length} pending, spawning ${slots} triager(s)`);

    // Track which labels we spawn so we can reconcile stuck claims after.
    const labels = [];
    const settled = new Set();
    const triagerResults = await Promise.all(
      Array.from({ length: slots }, (_, i) => {
        const label = `triage-w${wave}[${i}]`;
        labels.push(label);
        return withSlot(() =>
          agent(triagePrompt(label), {
            agent: "review-triage",
            label,
            phase: "Triage",
            schema: TRIAGE_DONE_SCHEMA,
            tools: READ_ONLY_TOOLS,
          }).then(r => {
            if (r) settled.add(label);
            return { label, ok: !!r, processed: r ? (r.processed || 0) : 0 };
          }),
        );
      }),
    );

    // Stale-claim sweep: any in-review finding whose `reviewer` matches a
    // label that did NOT settle cleanly (agent returned null — timeout,
    // abort, schema fail) is reverted to pending so the next wave can
    // retry it. Findings claimed by labels that DID settle are left as
    // in-review only if the triager forgot to keep/drop them — in which
    // case they'll be flagged in the final output's by_status.in_review.
    let reverted = 0;
    for (const f of inReview()) {
      if (f.reviewer && labels.includes(f.reviewer) && !settled.has(f.reviewer)) {
        f.status = "pending";
        f.reviewer = null;
        reverted++;
      }
    }
    if (reverted > 0) log(`triage wave ${wave}: reverted ${reverted} stuck claim(s) from failed triagers`);

    const totalProcessed = triagerResults.reduce((a, r) => a + r.processed, 0);
    const okCount        = triagerResults.filter(r => r.ok).length;
    log(`triage wave ${wave} done: ${okCount}/${triagerResults.length} triagers ok, ${totalProcessed} findings processed, ${pending().length} still pending`);

    // Deadlock detection: if a wave processed zero findings and no
    // claims were reverted, the next wave will see the same state.
    if (totalProcessed === 0 && reverted === 0) {
      log(`triage wave ${wave}: no progress (0 processed, 0 reverted). Stopping.`);
      break;
    }
  }
}

// One final orphaned-claim cleanup: any in-review finding left over
// after the loop (because the keep/drop call never landed) is reverted
// to pending so it shows up clearly in the output as un-triaged. We
// keep the reviewer label for debugging.
const orphanedClaims = inReview();
if (orphanedClaims.length > 0) {
  log(`WARN: ${orphanedClaims.length} finding(s) ended in 'in-review' (triager claimed but never decided). Marking as pending for visibility.`);
  for (const f of orphanedClaims) f.status = "pending";
}

// ──────────────────── FINAL OUTPUT ────────────────────
const summary = {
  meta: {
    project: PROJECT_ROOT,
    generated: new Date().toISOString(),
    review_prompt: REVIEW_PROMPT,
    buckets: PATH_BUCKETS.length,
  },
  total_reported: counter,
  by_status: {
    kept:    byStatus("kept"),
    dropped: byStatus("dropped"),
    pending: byStatus("pending"),
    in_review: byStatus("in-review"),
  },
  findings,
};

const outPath = path.isAbsolute(OUTPUT) ? OUTPUT : path.join(PROJECT_ROOT, OUTPUT);
await fs.writeFile(outPath, JSON.stringify(summary, null, 2), "utf8");
log(`wrote ${outPath}`);

return {
  ok: true,
  total_reported: counter,
  kept: summary.by_status.kept,
  dropped: summary.by_status.dropped,
  pending: summary.by_status.pending,
  output: outPath,
};
